public class BinarySearchTreeNode {
    public int data;
    public BinarySearchTreeNode left;
    public BinarySearchTreeNode right;

    public BinarySearchTreeNode(int data) {
        this.data = data;
        this.left = null;
        this.right = null;
    }

    public BinarySearchTreeNode() {
        this.data = 0;
        this.left = null;
        this.right = null;
    }
}
